package com.ocproj.projet.beans;

public class Palette {
	private int palette_id; 
	private String palette_name;
	private String palette_colors;
	public int getPalette_id() {
		return palette_id;
	}
	public void setPalette_id(int palette_id) {
		this.palette_id = palette_id;
	}
	public String getPalette_name() {
		return palette_name;
	}
	public void setPalette_name(String palette_name) {
		this.palette_name = palette_name;
	}
	public String getPalette_colors() {
		return palette_colors;
	}
	public void setPalette_colors(String palette_colors) {
		this.palette_colors = palette_colors;
	}
	
}
